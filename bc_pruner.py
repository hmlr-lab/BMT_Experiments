def _apply_explicit_constraints(
    bc_strings: List[str],
    constraints: Dict[str, List[str]],
    verbose: bool = False,
) -> List[str]:
    """
    Apply explicit predicate-trigger constraints.

    If a literal whose functor matches a key in `constraints` is present in
    the BC, every *other* literal whose functor starts with any of the
    associated prefix strings is removed.

    The trigger literal itself is never removed.

    Example
    -------
    constraints = {"starboard_forward": ["starboard", "forward"]}

    If "starboard_forward(A,B)" is present, remove any literal whose functor
    starts with "starboard" or "forward" — e.g. "starboard(A,B)",
    "forward(A,B)", "starboard_beam(A,B)" — but keep "starboard_forward(A,B)".
    """
    if not constraints:
        return bc_strings

    # Collect functors present in this BC
    present_functors: Set[str] = set()
    for s in bc_strings:
        lit = parse_literal(s)
        if lit is not None:
            present_functors.add(lit[0])

    # Build the set of removal prefixes from triggered constraints
    prefixes_to_remove: Set[str] = set()
    for trigger_functor, remove_prefixes in constraints.items():
        if trigger_functor in present_functors:
            prefixes_to_remove.update(remove_prefixes)
            if verbose:
                print(f"  EXPLICIT  trigger='{trigger_functor}' "
                      f"→ remove prefixes {remove_prefixes}")

    if not prefixes_to_remove:
        return bc_strings

    result = []
    for s in bc_strings:
        lit = parse_literal(s)
        functor = lit[0] if lit is not None else None
        if (functor
                and functor not in constraints          # never remove the trigger
                and any(functor.startswith(p) for p in prefixes_to_remove)):
            if verbose:
                print(f"  REMOVE  {s!r:40s}  "
                      f"← explicit constraint (prefix match)")
            continue
        result.append(s)
    return result


def prune_bottom_clauses_from_file(
    bk_file: str,
    bottom_clauses: Dict[str, List[str]],
    explicit_constraints: Optional[Dict[str, List[str]]] = None,
    verbose: bool = False,
) -> Dict[str, List[str]]:
    """
    Prune redundant literals from a dict of bottom clauses using BK rules
    loaded from a Prolog file, with an optional second pass of explicit
    predicate-trigger constraints.

    Parameters
    ----------
    bk_file              : path to the BK Prolog file (e.g. "BK_1.pl")

    bottom_clauses       : dict  { example_head_string: [literal_string, ...] }
                           e.g. {
                             "add_waypoint(agent_1,...).": [
                                 "sector(A,B,starboard_bow_forward)",
                                 "dcpa(A,B,very_close)",
                                 ...
                             ],
                           }

    explicit_constraints : optional dict  { trigger_functor: [prefix, ...] }
                           For each entry, if a literal with functor ==
                           trigger_functor exists in the BC, every *other*
                           literal whose functor starts with any listed prefix
                           is removed.  The trigger literal itself is kept.

                           Example:
                             {"starboard_forward": ["starboard", "forward"]}
                           → presence of starboard_forward(A,B) removes
                             starboard(A,B) and forward(A,B) (but not
                             starboard_forward itself).

                           Multiple triggers and multiple prefixes are fine:
                             {
                               "starboard_forward": ["starboard", "forward"],
                               "port_forward":      ["port", "forward"],
                             }

    verbose              : if True, print removal/keep justifications per BC

    Returns
    -------
    Dict with the same keys, each mapped to its pruned literal list.

    Raises
    ------
    FileNotFoundError  if bk_file does not exist.
    ValueError         if bk_file contains no parseable positive rules.

    Pipeline
    --------
    For each BC:
      1. BK-driven subsumption pruning  (remove definitionally subsumed lits,
                                         respecting multi-path heads)
      2. Explicit-constraint pruning    (remove trigger-implied lits)
    """
    with open(bk_file, "r", encoding="utf-8") as fh:
        source = fh.read()

    rules = parse_bk(source)
    if not rules:
        raise ValueError(f"No positive rules found in '{bk_file}'.")

    if verbose:
        print(f"Loaded {len(rules)} positive rules from '{bk_file}'.")
        mp = build_multi_path_set(rules)
        print(f"Multi-path heads ({len(mp)}): {sorted(mp)}")
        if explicit_constraints:
            print(f"Explicit constraints: {explicit_constraints}")
        print()

    result: Dict[str, List[str]] = {}
    for head, lits in bottom_clauses.items():
        # Pass 1 — BK subsumption + multi-path guard
        after_bk = prune_bottom_clause(lits, rules, verbose=verbose)
        # Pass 2 — explicit trigger-based removal
        after_explicit = _apply_explicit_constraints(
            after_bk, explicit_constraints or {}, verbose=verbose
        )
        result[head] = after_explicit
    return result


def _banner(text: str, width: int = 70) -> str:
    return f"\n{'═' * width}\n  {text}\n{'═' * width}"


