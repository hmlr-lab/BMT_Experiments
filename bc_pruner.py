

import re
import itertools
from typing import List, Tuple, Dict, Optional, Set

# ─── Type aliases ────────────────────────────────────────────────────────────
Literal  = Tuple[str, Tuple[str, ...]]   # (functor, (arg, ...))
Rule     = Tuple[Literal, List[Literal]] # (head, [body_lit, ...])

_fresh_counter = itertools.count()


# ═══════════════════════════════════════════════════════════════════════════════
# Prolog parser
# ═══════════════════════════════════════════════════════════════════════════════

def _split_args(s: str) -> List[str]:
    """Split comma-separated Prolog args, respecting nested parentheses."""
    args, current, depth = [], "", 0
    for ch in s:
        if   ch == "(": depth += 1
        elif ch == ")": depth -= 1
        if ch == "," and depth == 0:
            args.append(current.strip())
            current = ""
        else:
            current += ch
    if current.strip():
        args.append(current.strip())
    return args


def parse_literal(s: str) -> Optional[Literal]:
    """
    Parse a Prolog literal string into (functor, args).
    Returns None for non-literal tokens (arithmetic, operators, etc.).
    """
    s = s.strip()
    if not s:
        return None
    # functor(args)
    m = re.match(r"^([A-Za-z_]\w*)\((.+)\)$", s, re.DOTALL)
    if m:
        return (m.group(1), tuple(_split_args(m.group(2))))
    # atom with no args
    if re.match(r"^[a-z_]\w*$", s):
        return (s, ())
    return None  # arithmetic, operators, variables alone, etc.


def _parse_body(body_str: str) -> List[Literal]:
    """
    Parse a rule body, skipping negated literals (not/\+) and arithmetic.
    Only positive, callable literals are returned.
    """
    lits = []
    for part in _split_args(body_str):
        part = part.strip()
        # Skip negation-as-failure wrappers
        if re.match(r"^(not|\\\\+)\s*\(", part):
            continue
        lit = parse_literal(part)
        if lit is not None:
            lits.append(lit)
    return lits


def parse_bk(source: str) -> List[Rule]:
    """
    Parse a Prolog BK string (not a filename) and return all rules.
    Directives (:- dynamic …) and bare facts are ignored.
    """
    # Strip line comments
    source = re.sub(r"%[^\n]*", "", source)
    # Split on clause-terminating dot (dot followed by whitespace / end of string)
    clauses = re.split(r"\.\s*(?:\n|$)", source)

    rules: List[Rule] = []
    for clause in clauses:
        clause = clause.strip()
        if not clause:
            continue
        if clause.startswith(":-"):        # directive
            continue
        if ":-" not in clause:             # bare fact — no body to exploit
            continue
        head_str, body_str = clause.split(":-", 1)
        head = parse_literal(head_str.strip())
        if head is None:
            continue
        body = _parse_body(body_str.strip())
        if body:                           # only keep rules with parseable positive body
            rules.append((head, body))
    return rules


# ═══════════════════════════════════════════════════════════════════════════════
# Unification
# ═══════════════════════════════════════════════════════════════════════════════

def _is_var(t: str) -> bool:
    """
    Prolog variable: starts with uppercase letter, OR is a fresh _RV* name
    generated by alpha-renaming.
    """
    return bool(t) and (t[0].isupper() or t.startswith("_RV"))


def _walk(t: str, subst: Dict[str, str]) -> str:
    seen: Set[str] = set()
    while _is_var(t) and t in subst:
        if t in seen:
            break           # cycle guard (shouldn't happen in our use-case)
        seen.add(t)
        t = subst[t]
    return t


def _unify(t1: str, t2: str, subst: Dict[str, str]) -> Optional[Dict[str, str]]:
    """
    Unify two terms under subst.

    Calling convention: t1 comes from the *bottom clause*, t2 from the *BK
    rule* (after alpha-renaming).  When both terms are variables we always
    bind the rule variable (t2 / _RV*) to the BC variable (t1 / A,B,…) so
    that apply_subst on the rule body faithfully translates rule vars into
    the BC's variable names.
    """
    t1, t2 = _walk(t1, subst), _walk(t2, subst)
    if t1 == t2:
        return subst
    if _is_var(t1) and _is_var(t2):
        # Prefer rule-var → BC-var direction
        if t2.startswith("_RV"):
            return {**subst, t2: t1}
        return {**subst, t1: t2}
    if _is_var(t1):
        return {**subst, t1: t2}
    if _is_var(t2):
        return {**subst, t2: t1}
    return None          # two distinct ground atoms — clash


def _unify_literals(l1: Literal, l2: Literal) -> Optional[Dict[str, str]]:
    f1, a1 = l1
    f2, a2 = l2
    if f1 != f2 or len(a1) != len(a2):
        return None
    subst: Dict[str, str] = {}
    for x, y in zip(a1, a2):
        subst = _unify(x, y, subst)   # type: ignore[assignment]
        if subst is None:
            return None
    return subst


def _apply_subst(lit: Literal, subst: Dict[str, str]) -> Literal:
    pred, args = lit
    return (pred, tuple(_walk(a, subst) for a in args))


# ═══════════════════════════════════════════════════════════════════════════════
# Alpha-renaming
# ═══════════════════════════════════════════════════════════════════════════════

def _alpha_rename_rule(head: Literal, body: List[Literal]) -> Rule:
    """
    Replace every variable in a rule with a globally fresh name so that
    rule-internal variables never collide with bottom-clause variables (A, B …).
    """
    # Collect all variable names appearing anywhere in the rule
    all_lits = [head] + body
    seen_vars: Set[str] = set()
    for _, args in all_lits:
        for a in args:
            if _is_var(a):
                seen_vars.add(a)

    rename = {v: f"_RV{next(_fresh_counter)}" for v in seen_vars}

    def _rename(lit: Literal) -> Literal:
        pred, args = lit
        return (pred, tuple(rename.get(a, a) for a in args))

    return _rename(head), [_rename(b) for b in body]


# ═══════════════════════════════════════════════════════════════════════════════
# Canonical string
# ═══════════════════════════════════════════════════════════════════════════════

def lit_str(lit: Literal) -> str:
    pred, args = lit
    return f"{pred}({','.join(args)})" if args else pred


# ═══════════════════════════════════════════════════════════════════════════════
# Multi-path detection
# ═══════════════════════════════════════════════════════════════════════════════

def _head_canon(head: Literal) -> str:
    """
    Canonical key for grouping rules by their head.
    Variables are replaced with '_'; constants are kept verbatim.

    Examples
    --------
    applies(X,Y,rule16_giveway)     →  applies(_,_,rule16_giveway)
    dcpa_unsafe(X,Y)                →  dcpa_unsafe(_,_)
    sector(X,Y,starboard_bow_forward) → sector(_,_,starboard_bow_forward)
    """
    pred, args = head
    canon = ["_" if _is_var(a) else a for a in args]
    return f"{pred}({','.join(canon)})" if canon else pred


def build_multi_path_set(rules: List[Rule]) -> Set[str]:
    """
    Return the set of head-canonical strings whose defining rules have
    *structurally diverse* positive bodies — i.e. the set of positive body
    functors differs across at least two rules for the same head pattern.

    Rationale
    ---------
    If every rule for H shares the same body-functor signature (only the
    *constants* differ, as in dcpa_unsafe or risk_collision), the body
    literals are just instance witnesses and can safely be pruned once H
    is present.

    If at least two rules differ in which *predicates* they use
    (e.g. applies(_,_,rule16_giveway) can come from overtaking, headon,
    OR crossing+starboard), the body literals are *discriminative*: they
    identify which derivation path was actually taken.  Removing them
    would lose information the ILP learner needs.

    Examples
    --------
    dcpa_unsafe(_,_)
        bodies: {dcpa}/{dcpa}/{dcpa}/{dcpa}  — same structure → NOT multi-path

    applies(_,_,rule16_giveway)
        bodies: {applies}/{applies}/{applies,starboard} — varies → MULTI-PATH

    applies(_,_,rule17a1_standon_maintain)
        bodies: {applies}/{applies,port}                — varies → MULTI-PATH
    """
    # Group rules by head-canonical string
    groups: Dict[str, List[Rule]] = {}
    for rule in rules:
        key = _head_canon(rule[0])
        groups.setdefault(key, []).append(rule)

    multi_path: Set[str] = set()
    for key, grp in groups.items():
        if len(grp) <= 1:
            continue
        # Positive body functor-sets for each rule in the group
        functor_sets = [
            frozenset(b[0] for b in body)   # b[0] = functor
            for _, body in grp
        ]
        if len(set(functor_sets)) > 1:      # at least two distinct signatures
            multi_path.add(key)

    return multi_path


# ═══════════════════════════════════════════════════════════════════════════════
# Core pruner
# ═══════════════════════════════════════════════════════════════════════════════

def prune_bottom_clause(
    bc_strings: List[str],
    rules: List[Rule],
    verbose: bool = False,
) -> List[str]:
    """
    Parameters
    ----------
    bc_strings : list of literal strings from the bottom clause
    rules      : parsed BK rules  (use parse_bk)
    verbose    : print justification for every removed / skipped literal

    Returns
    -------
    Pruned list of literal strings (original order preserved).

    Algorithm
    ---------
    For every literal L in the BC:
        If L's head-canonical form is MULTI-PATH:
            skip — body literals are discriminative, keep them.
        Else for every BK rule  H :- b1, …, bk:
            If L unifies with H under σ:
                mark every σ(bi) present in the BC for removal.
    Return BC minus the marked set.
    """
    # Pre-compute multi-path head patterns
    multi_path = build_multi_path_set(rules)
    if verbose:
        print("  Multi-path heads (bodies kept as discriminative witnesses):")
        for mp in sorted(multi_path):
            print(f"    {mp}")
        print()

    # Parse every BC literal; keep the original string for output
    parsed_bc: List[Tuple[str, Literal]] = []
    for s in bc_strings:
        lit = parse_literal(s)
        if lit is not None:
            parsed_bc.append((s, lit))

    # Quick membership test (canonical form)
    bc_canon: Set[str] = {lit_str(l) for _, l in parsed_bc}

    # Collect what should be removed and why (for verbose output)
    to_remove: Dict[str, str] = {}   # canonical_str → reason

    for orig_s, bc_lit in parsed_bc:
        # ── Multi-path guard ──────────────────────────────────────────────────
        lit_canon = _head_canon(bc_lit)
        if lit_canon in multi_path:
            if verbose:
                print(f"  KEEP    {orig_s!r:40s}  (multi-path head — "
                      f"body literals are discriminative)")
            continue

        # ── Standard subsumption pruning ─────────────────────────────────────
        for raw_head, raw_body in rules:
            head, body = _alpha_rename_rule(raw_head, raw_body)

            subst = _unify_literals(bc_lit, head)
            if subst is None:
                continue

            for b_lit in body:
                grounded = _apply_subst(b_lit, subst)
                gs = lit_str(grounded)
                if gs in bc_canon and gs not in to_remove:
                    reason = (
                        f"subsumed by '{orig_s}' "
                        f"via rule [{lit_str(raw_head)} :- "
                        f"{', '.join(lit_str(bl) for bl in raw_body)}]"
                    )
                    to_remove[gs] = reason
                    if verbose:
                        print(f"  REMOVE  {gs!r:40s}  ← {reason}")

    pruned = [s for s in bc_strings
              if parse_literal(s) is None
              or lit_str(parse_literal(s)) not in to_remove]
    return pruned


